# Fibonacci Sequence Implementation in MIPS
# This program calculates Fibonacci numbers and stores them in memory

.data
    fib_array: .word 0 : 10      # حجز مساحة لـ 10 أرقام في الذاكرة
    size:      .word 10          # عدد العناصر المطلوب حسابها

.text
.globl main

main:
    # تحميل العنوان الأساسي للمصفوفة وحجمها
    la $t0, fib_array          # $t0 = عنوان المصفوفة
    lw $t5, size               # $t5 = الحجم (10)

    # إعداد أول رقمين في السلسلة (0 و 1)
    li $t2, 0                  # الرقم الأول
    li $t3, 1                  # الرقم الثاني
    sw $t2, 0($t0)             # تخزين الرقم الأول في fib_array[0]
    sw $t3, 4($t0)             # تخزين الرقم الثاني في fib_array[1]

    # إعداد العداد للحلقة (Loop)
    addi $t1, $t5, -2          # $t1 = الحجم - 2 (لأننا خزنّا أول رقمين)
    addi $t0, $t0, 8           # تحريك مؤشر المصفوفة للموقع الثالث

loop:
    add $t4, $t2, $t3          # $t4 = المجموع (الرقم الحالي)
    sw $t4, 0($t0)             # تخزين النتيجة في المصفوفة
    
    # تحديث القيم للخطوة القادمة
    move $t2, $t3              # الرقم الأول القديم يصبح الثاني
    move $t3, $t4              # الرقم الثاني القديم يصبح المجموع
    
    addi $t0, $t0, 4           # الانتقال للعنصر التالي في المصفوفة
    addi $t1, $t1, -1          # إنقاص العداد
    bgtz $t1, loop             # تكرار الحلقة إذا كان العداد أكبر من 0

    # إنهاء البرنامج
    li $v0, 10
    syscall
